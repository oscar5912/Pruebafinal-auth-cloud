class PatientForm(forms.ModelForm):
  
    #este es el campo adicional para vincular al paciente y su médico asistente
     #esto mostrará el modelo desplegable del método __str__ médico que se muestra en html, así que anúlelo
     #to_field_name esto buscará el valor correspondiente user_id presente en el modelo Doctor y lo devolverá
    assignedDoctorId=forms.ModelChoiceField(queryset=models.Doctor.objects.all().filter(status=True),empty_label="Name and Department", to_field_name="user_id")
    class Meta:
        model=models.Patient
        fields=['address','mobile','status','symptoms','profile_pic']



class AppointmentForm(forms.ModelForm):
    doctorId=forms.ModelChoiceField(queryset=models.Doctor.objects.all().filter(status=True),empty_label="Doctor Name and Department", to_field_name="user_id")
    patientId=forms.ModelChoiceField(queryset=models.Patient.objects.all().filter(status=True),empty_label="Patient Name and Symptoms", to_field_name="user_id")
    class Meta:
        model=models.Appointment
        fields=['description','status']


